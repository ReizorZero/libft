#include "libft.h"

void	ft_putnbr(int n)
{
	char digit;

	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	if (n == -2147483648)
		ft_putstr("2147483648");
	else
	{
		if (n >= 10)
			ft_putnbr(n / 10);
		digit = (n % 10) + '0';
		write(1, &digit, 1);
	}
}
