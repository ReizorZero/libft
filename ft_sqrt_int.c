int		ft_sqrt_int(int nb)
{
	int sqrt;

	sqrt = nb;
	while (sqrt > 0)
	{
		if (nb % sqrt == 0 && sqrt * sqrt == nb)
			return (sqrt);
		else
			sqrt--;
	}
	return (0);
}
